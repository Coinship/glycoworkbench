package org.eurocarbdb.application.glycoworkbench.analytics;

public class UnableToDecompressLogFileException extends Exception {

	public UnableToDecompressLogFileException() {
	}

	public UnableToDecompressLogFileException(String message) {
		super(message);

	}

	public UnableToDecompressLogFileException(Throwable cause) {
		super(cause);

	}

	public UnableToDecompressLogFileException(String message, Throwable cause) {
		super(message, cause);

	}

	public UnableToDecompressLogFileException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

}
