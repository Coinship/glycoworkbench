package org.eurocarbdb.application.glycoworkbench.analytics;

public class UnableToReadErrorLogFileException extends Exception {

	public UnableToReadErrorLogFileException() {
	}

	public UnableToReadErrorLogFileException(String message) {
		super(message);

	}

	public UnableToReadErrorLogFileException(Throwable cause) {
		super(cause);

	}

	public UnableToReadErrorLogFileException(String message, Throwable cause) {
		super(message, cause);

	}

	public UnableToReadErrorLogFileException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

}
