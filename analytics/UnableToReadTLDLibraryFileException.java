package org.eurocarbdb.application.glycoworkbench.analytics;

public class UnableToReadTLDLibraryFileException extends Exception {

	public UnableToReadTLDLibraryFileException() {
	}

	public UnableToReadTLDLibraryFileException(String message) {
		super(message);

	}

	public UnableToReadTLDLibraryFileException(Throwable cause) {
		super(cause);

	}

	public UnableToReadTLDLibraryFileException(String message, Throwable cause) {
		super(message, cause);

	}

	public UnableToReadTLDLibraryFileException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

}
